#include<stdio.h>

int main() {
    float a = 0.617;
    int b = 2;
    float c = 19;
    int d = 3;
    float e = 124.555;
    float f = 0.001;

    printf("a = %.3f\n", a); // แสดงค่าของ a ทศนิยม 3 ตำแหน่ง
    printf("b = %d\n", b); // แสดงค่าของ b เป็นจำนวนเต็ม
    printf("c = %.2f\n", c); // แสดงค่าของ c ทศนิยม 2 ตำแหน่ง
    printf("d = %d\n", d); // แสดงค่าของ d เป็นจำนวนเต็ม
    printf("e = %.3f\n", e); // แสดงค่าของ e ทศนิยม 3 ตำแหน่ง
    printf("f = %.3f\n", f); // แสดงค่าของ f ทศนิยม 3 ตำแหน่ง 
}